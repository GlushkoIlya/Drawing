def el(m, n, x):
    t.up()
    t.goto(m, n)
    t.fd(x / 2)
    t.down()
    for i in range(3):
        t.right(120)
        t.fd(x)
        t.left(120)
        t.fd(x)
        t.left(120)
        t.fd(x)
        t.left(120)
        t.fd(x)
        t.left(120)
        t.fd(x/2)
        x += x / 2
    t.fd(x/16)
    t.right(90)
    t.fd(x / 4)
    t.right(90)
    t.fd(x/8)
    t.right(90)
    t.fd(x / 4)


